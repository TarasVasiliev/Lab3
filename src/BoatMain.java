import java.util.Arrays;
import java.util.Comparator;

/**
 * Клас BoatMain запускає програму для роботи з масивом об'єктів класу Boats.
 * Програма сортує масив човнів за заданими критеріями та шукає ідентичний човен
 * в відсортованому масиві.
 */
public class BoatMain {
    /**
     * Основний метод програми.
     * Створює масив човнів, сортує його за довжиною (в порядку зростання)
     * і місткістю (в порядку спадання), а потім здійснює пошук ідентичного човна.
     *
     * @param args аргументи командного рядка (не використовуються в даній програмі)
     */
    public static void main(String[] args) {
        // Створення масиву об'єктів Boats з різними характеристиками
        Boats[] boats = {
                new Boats("Дослідник", 10.5, 6, 30.0, "Моторний"),
                new Boats("Моряк", 15.0, 8, 20.0, "Вітрильник"),
                new Boats("Швидкісний", 8.0, 4, 40.0, "Швидкісний"),
                new Boats("Вантажний", 20.0, 20, 15.0, "Вантажний"),
                new Boats("Круїзний", 12.0, 10, 25.0, "Круїзний")
        };

        // Сортування масиву човнів за довжиною (зростаючий порядок),
        // і в разі рівної довжини - за місткістю (спадаючий порядок)
        Arrays.sort(boats, Comparator.comparingDouble(Boats::getLength)
                .thenComparing(Comparator.comparingInt(Boats::getCapacity).reversed()));

        // Виведення відсортованого списку човнів
        System.out.println("Відсортовані човни:");
        for (Boats boat : boats) {
            System.out.println(boat);
        }

        // Створення цільового човна для пошуку в масиві
        Boats targetBoat = new Boats("Круїзний", 12.0, 10, 25.0, "Круїзний");

        // Змінна для відстеження, чи знайдено ідентичний човен
        boolean found = false;

        // Перебір масиву човнів для пошуку ідентичного човна
        for (Boats boat : boats) {
            if (boat.equals(targetBoat)) {
                // Якщо ідентичний човен знайдено, вивести повідомлення та завершити пошук
                System.out.println("\nЗнайдено ідентичний човен: " + boat);
                found = true;
                break;
            }
        }

        // Якщо ідентичний човен не знайдено, вивести відповідне повідомлення
        if (!found) {
            System.out.println("\nІдентичний човен не знайдено.");
        }
    }
}
